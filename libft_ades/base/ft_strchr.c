#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	const unsigned char	chr = c;

	while (*s)
	{
		if (*s == chr)
			return ((char *)s);
		s++;
	}
	if (chr == '\0')
		return ((char *)s);
	return (NULL);
}
